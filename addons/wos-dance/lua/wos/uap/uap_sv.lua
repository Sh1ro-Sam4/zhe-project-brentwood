util.AddNetworkString( "wOS.UAP.Stop" )
net.Receive( "wOS.UAP.Stop", function( len, ply )
	ply:SetNWBool( "wOS.UAP.AnimActive", false )
end )

concommand.Add( "wos_uap", function( ply, cmd, args )
	if not ply:Alive() or not IsValid( ply ) or ply:InVehicle() then return end
	if args[1] == "stop" then ply:SetNWBool( "wOS.UAP.AnimActive", false ) return end
	if args[2] then return end
	local uap_sequence = ""
	if table.HasValue( uap_anims_fortnite, args[1] ) then
		uap_sequence = "wos_uap_fortnite_"..args[1]
	elseif table.HasValue( uap_anims_other, args[1] ) then
		uap_sequence = "wos_uap_other_"..args[1]
	elseif table.HasValue( uap_anims_mocap, args[1] ) then
		uap_sequence = "wos_uap_mocap_"..args[1]
	elseif table.HasValue( uap_anims_tf2, args[1] ) then
		uap_sequence = "wos_uap_tf2_"..args[1]
	else
		uap_sequence = "INVALID"
		print( "Invalid sequence name." )
	end
	if uap_sequence == "INVALID" then return end
	ply:SetNWString( "wOS.UAP.SequenceID", uap_sequence )
	ply:SetNWBool( "wOS.UAP.AnimActive", true )
	ply:SetNWBool( "wOS.UAP.ResetSequence", false )
	timer.Simple( 0.1, function() ply:SetNWBool( "wOS.UAP.ResetSequence", true ) end )
end )

