local lastThink = 0
__DAY_TIME = __DAY_TIME or false
DayMult = DayMult or 1
local function _cvThink()
    if lastThink < CurTime() and GetGlobalBool("ChangeLight", false) then
        lastThink = CurTime() + .2
        render.RedownloadAllLightmaps()
    end

    __DAY_TIME = GetGlobalBool("DayTime", false)
    DayMult = __DAY_TIME and math.min( 1, DayMult + .01 ) or math.max( 0, DayMult - .01 )
end

local function _cvCalcFog()
    render.FogMode( 1 )
    render.FogStart( Lerp( DayMult, 512, 2048 ) )
    render.FogEnd( Lerp( DayMult, 2048, 8192 ) )
    render.FogMaxDensity( 1 )

    local clr = shizlib.surface.LerpColor( DayMult, Color( 0, 0, 5 ), Color( 99, 167, 255 ) )
    render.FogColor( clr.r, clr.g, clr.b )
    return true
end

local function _cvCalcSkyboxFog()
    render.FogMode( 1 )
    render.FogStart( Lerp( DayMult, 32, 128 ) )
    render.FogEnd( Lerp( DayMult, 128, 512 ) )
    render.FogMaxDensity( 1 )

    local clr = shizlib.surface.LerpColor( DayMult, Color( 0, 0, 5 ), Color( 99, 167, 255 ) )
    render.FogColor( clr.r, clr.g, clr.b )
    return true
end

