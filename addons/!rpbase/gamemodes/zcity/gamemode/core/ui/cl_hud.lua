function GM:ArrestHud()
    if lply.organism and lply.organism.otrub then return end
    if not lply:Alive() then return end
    if not lply:GetNWBool("is_arrested") then return end

    local arrestEndTime = lply:GetNWInt("arrest_end_time")
    if arrestEndTime <= CurTime() then return end

    local remainingTime = math.max(0, arrestEndTime - CurTime())
    local minutes = math.floor(remainingTime / 60)
    local seconds = math.floor(remainingTime % 60)
    local timeText = string.format("%02d:%02d", minutes, seconds)

    local x = scrw / 2
    local y = scrh / 2 + s(200)
    local width = s(200)
    local height = s(120)

    RNDX.Draw(6, x - width / 2, y - height / 2, width, height, nil, RNDX.BLUR)
    RNDX.Draw(6, x - width / 2, y - height / 2, width, height, ColorAlpha(CFG.theme.bg, 200))
    draw.DrawText("ВЫ АРЕСТОВАНЫ", "ui.24", x, y - s(35), Color(255, 50, 50), TEXT_ALIGN_CENTER)
    draw.DrawText("Осталось: " .. timeText, "ui.18", x, y - s(5), Color(255, 255, 100), TEXT_ALIGN_CENTER)
    draw.DrawText("Причина: " .. (lply:GetNWString("arrest_reason") or "Не указано"), "ui.18", x, y + s(20), Color(255, 255, 255), TEXT_ALIGN_CENTER)
end



